package Package;

public class LibraryBean {
	private String bookname;
	private String fristfind;
	private String secondfind;
	private String rentalday;
	private String returnday;
	private String canrent;
	
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public String getFristfind() {
		return fristfind;
	}
	public void setFristfind(String fristfind) {
		this.fristfind = fristfind;
	}
	public String getSecondfind() {
		return secondfind;
	}
	public void setSecondfind(String secondfind) {
		this.secondfind = secondfind;
	}
	public String getRentalday() {
		return rentalday;
	}
	public void setRentalday(String rentalday) {
		this.rentalday = rentalday;
	}
	public String getReturnday() {
		return returnday;
	}
	public void setReturnday(String returnday) {
		this.returnday = returnday;
	}
	public String getCanrent() {
		return canrent;
	}
	public void setCanrent(String canrent) {
		this.canrent = canrent;
	}
	
}
